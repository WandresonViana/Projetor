public class DataShow {
    private int id;
    private String descircao;

    public DataShow() {
    }

    public DataShow(int id, String descircao) {
        this.id = id;
        this.descircao = descircao;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDescircao() {
        return descircao;
    }

    public void setDescircao(String descircao) {
        this.descircao = descircao;
    }

}
